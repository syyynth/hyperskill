-- Drop tables if they exist
DROP TABLE IF EXISTS Painting;
DROP TABLE IF EXISTS Square;
DROP TABLE IF EXISTS Spray;


-- Create Square table
CREATE TABLE Square (
    id INT PRIMARY KEY,
    name VARCHAR(35) NOT NULL
);

-- Create Spray table
CREATE TABLE Spray (
    id INT PRIMARY KEY,
    name VARCHAR(35) NOT NULL,
    color CHAR(1) CHECK (color IN ('R', 'G', 'B')) NOT NULL
);

-- Create Painting table
CREATE TABLE Painting (
    datetime TIMESTAMP,
    square_id INT,
    spray_id INT,
    volume INT CHECK (volume > 0 AND volume <= 256) NOT NULL,
    PRIMARY KEY (datetime, square_id, spray_id),
    FOREIGN KEY (square_id) REFERENCES Square(id),
    FOREIGN KEY (spray_id) REFERENCES Spray(id)
);

